package class2;

public class ZooAnimal {
	private String animalName;
	private String species; 
	private String featur; 
	private String whatYouLikedBestAboutTheAnimal;
	
	
	public String getAnimalName() {
		return animalName;
	}
	public void setAnimalName(String animalName) {
		this.animalName = animalName;
	}
	public String getSpecies() {
		return species;
	}
	public void setSpecies(String species) {
		this.species = species;
	}
	public String getFeatur() {
		return featur;
	}
	public void setFeatur(String featur) {
		this.featur = featur;
	}
	public String getWhatYouLikedBestAboutTheAnimal() {
		return whatYouLikedBestAboutTheAnimal;
	}
	public void setWhatYouLikedBestAboutTheAnimal(String whatYouLikedBestAboutTheAnimal) {
		this.whatYouLikedBestAboutTheAnimal = whatYouLikedBestAboutTheAnimal;
	} 
	
	
}
